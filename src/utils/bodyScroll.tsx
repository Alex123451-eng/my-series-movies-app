export const hideBodyScroll = () => (document.body.style.overflow = "hidden");
export const showBodyScroll = () => (document.body.style.overflow = "auto");
export const changeBodyColorToWhite = () =>
  (document.body.style.background = "rgba(255, 255, 255, 1)");
export const changeBodyColorToBlack = () =>
  (document.body.style.background = "rgba(16, 14, 25, 1)");
